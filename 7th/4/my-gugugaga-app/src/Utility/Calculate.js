const add = (First , second) => {
    return First + second
}
const subtract = (First , second) => {
    return First - second
}
const multiplication = (First , second) => {
    return First * second
}
const div = (First , second) => {
    return First / second
}
const calculate = {add:add , subtract:subtract,multiplication:multiplication, div:div}
export default calculate




